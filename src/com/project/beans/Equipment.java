package com.project.beans;

public class Equipment {

	int equipmentID;
	String equipmentName;
	String equipmentProvider;
	long equipmentContact;
	float equipmentPrice;
	public int getEquipmentID() {
		return equipmentID;
	}
	public void setEquipmentID(int equipmentID) {
		this.equipmentID = equipmentID;
	}
	public String getEquipmentName() {
		return equipmentName;
	}
	public void setEquipmentName(String equipmentName) {
		this.equipmentName = equipmentName;
	}
	public String getEquipmentProvider() {
		return equipmentProvider;
	}
	public void setEquipmentProvider(String equipmentProvider) {
		this.equipmentProvider = equipmentProvider;
	}
	public long getEquipmentContact() {
		return equipmentContact;
	}
	public void setEquipmentContact(long equipmentContact) {
		this.equipmentContact = equipmentContact;
	}
	public float getEquipmentPrice() {
		return equipmentPrice;
	}
	public void setEquipmentPrice(float equipmentPrice) {
		this.equipmentPrice = equipmentPrice;
	}
	
	
}
